# ─── Public API ──────────────────────────────────────────────────────────────

# Scaffold a single editor's project config.
# Usage: flowai_scaffold_editor_config <project_root> <editor_name>
# editor_name: claude | gemini | cursor | copilot
flowai_scaffold_editor_config() {
  local root="$1"
  local editor="$2"

  case "$editor" in
    claude)  _scaffold_claude  "$root" ;;
    gemini)  _scaffold_gemini  "$root" ;;
    cursor)  _scaffold_cursor  "$root" ;;
    copilot) _scaffold_copilot "$root" ;;
    *)
      log_warn "Unknown editor '$editor' — skipping scaffold."
      return 1
      ;;
  esac
}

# All supported editor names (used by the interactive chooser).
_SCAFFOLD_EDITORS=(claude gemini cursor copilot)

# Human-readable label for display.
_scaffold_editor_label() {
  case "$1" in
    claude)  echo "Claude  (.claude/CLAUDE.md)" ;;
    gemini)  echo "Gemini  (.gemini/GEMINI.md)" ;;
    cursor)  echo "Cursor  (.cursor/rules/flowai.mdc)" ;;
    copilot) echo "Copilot (.github/copilot-instructions.md)" ;;
  esac
}

# Interactive: scaffold primary tool + ask about others.
# Usage: flowai_scaffold_editor_interactive <project_root> <primary_tool>
flowai_scaffold_editor_interactive() {
  local root="$1"
  local primary="$2"

  log_header "Editor Config Scaffolding"
  printf '\n'

  # 1. Always scaffold the primary tool
  log_info "Creating project context for primary tool: $primary"
  flowai_scaffold_editor_config "$root" "$primary" || true

  # 2. Collect other editors
  local others=()
  for e in "${_SCAFFOLD_EDITORS[@]}"; do
    [[ "$e" == "$primary" ]] && continue
    others+=("$e")
  done

  if [[ ${#others[@]} -eq 0 ]]; then
    return 0
  fi

  # 3. Automatically scaffold all other supported editors for team compatibility
  printf '\n'
  log_info "Creating context files for other AI editors your team may use:"

  for e in "${others[@]}"; do
    flowai_scaffold_editor_config "$root" "$e" || true
  done

  printf '\n'
}

# Non-interactive: scaffold only the primary tool (for testing / CI).
# Usage: flowai_scaffold_editor_noninteractive <project_root> <primary_tool>
flowai_scaffold_editor_noninteractive() {
  local root="$1"
  local primary="$2"
  flowai_scaffold_editor_config "$root" "$primary" || true
}
