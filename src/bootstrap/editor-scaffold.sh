#!/usr/bin/env bash
# FlowAI — AI editor project-config scaffolding
#
# Each AI editor expects its own project-level context directory/file:
#   Claude  → .claude/CLAUDE.md
#   Gemini  → .gemini/GEMINI.md
#   Cursor  → .cursor/rules/flowai.mdc
#   Copilot → .github/copilot-instructions.md
#
# This module creates starter files for each editor so that agents immediately
# understand the project structure, FlowAI conventions, and where to find
# specs, config, and the knowledge graph.
#
# API:
#   flowai_scaffold_editor_config <project_root> <editor_name>
#   flowai_scaffold_editor_interactive <project_root> <primary_tool>
#
# shellcheck shell=bash

# shellcheck source=src/core/log.sh
source "$FLOWAI_HOME/src/core/log.sh"

# ─── Guard: never overwrite ──────────────────────────────────────────────────

_scaffold_write_if_absent() {
  local dest="$1"
  local content="$2"
  if [[ -f "$dest" ]]; then
    log_info "  ↳ Already exists — skipped: ${dest##*/}"
    return 0
  fi
  mkdir -p "$(dirname "$dest")"
  printf '%s\n' "$content" > "$dest"
  log_success "  ↳ Created: ${dest##*/}"
  return 0
}

# ─── Template: shared header ─────────────────────────────────────────────────

_scaffold_header() {
  local project_name="$1"
  cat <<EOF
# ${project_name} — FlowAI Project Context

> Auto-generated by \`flowai init\`. Customize freely — FlowAI will never overwrite this file.

## Project Structure

- **\`.flowai/config.json\`** — FlowAI configuration (tools, models, roles, pipeline phases)
- **\`specs/\`** — Feature specifications (business intent, acceptance criteria)
- **\`.flowai/wiki/graph.json\`** — Knowledge graph (run \`flowai graph build\`; override with \`graph.wiki_dir\`)
- **\`.flowai/roles/\`** — Local role prompt overrides
EOF
}

# ─── Template: Claude ────────────────────────────────────────────────────────

_scaffold_claude() {
  local root="$1"
  local project_name
  project_name="$(basename "$root")"
  local dest="$root/.claude/CLAUDE.md"

  local content
  content="$(_scaffold_header "$project_name")

## FlowAI Integration

This project uses **FlowAI** for multi-agent orchestration. Key commands:
- \`flowai start\` — Launch the tmux agent session
- \`flowai graph build\` — Build/update the knowledge graph
- \`flowai run <phase>\` — Run a specific pipeline phase (plan, tasks, impl, review)

## Conventions

- Follow the spec-driven development workflow: specs → plan → tasks → implement → review
- Reference spec IDs (e.g. \`UC-AUTH-001\`) in commit messages to link code to business intent
- Use \`flowai graph query \"...\"\` to explore codebase relationships before making changes
"

  _scaffold_write_if_absent "$dest" "$content"
}

